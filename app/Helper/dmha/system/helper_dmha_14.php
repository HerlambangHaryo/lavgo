    function dmha_14_generate_input_type($dmha_14,$class,$dmha_13,$name)
    {
        // ------------------------------------------------------------------------- INITIALIZE
            $isi    = '';

        // ------------------------------------------------------------------------- ACTION
            if($dmha_14 == 1)
            { $isi .= '<input type="text" class="'.$class.'" id="form-'.$dmha_13.'" name="'.$name.'">';  }  

            elseif($dmha_14 == 2)
            { $isi .= '<input type="number" class="'.$class.'" id="form-'.$dmha_13.'"  name="'.$name.'">';  }     

            elseif($dmha_14 == 3)
            {   $isi .= '
                <select class="form-control"  id="form-'.$dmha_13.'"  name="'.$name.'">';
                $isi .= '<option value="1">1</option>';
                $isi .= '
                </select>';  }        

