<?php
    
 
    function open_form($class){
        // ------------------------------------------------------------------------- INITIALIZE
            $isi = '<form class="'.$class.'" action="" method="POST">';
        // ------------------------------------------------------------------------- ACTION

        // ------------------------------------------------------------------------- SEND
        	$word = $isi;
        	return $word;            
        ////////////////////////////////////////////////////////////////////////////        
    }

    function open_form_enctype(){
        // ------------------------------------------------------------------------- INITIALIZE
            $isi = '<form class="form-horizontal form-bordered" action="" method="POST" enctype="multipart/form-data">';
        // ------------------------------------------------------------------------- ACTION

        // ------------------------------------------------------------------------- SEND
            $word = $isi;
            return $word;            
        ////////////////////////////////////////////////////////////////////////////        
    }

    function close_form(){
        // ------------------------------------------------------------------------- INITIALIZE
    		$isi = '</form>';
        // ------------------------------------------------------------------------- ACTION

        // ------------------------------------------------------------------------- SEND
        	$word = $isi;
        	return $word;            
        ////////////////////////////////////////////////////////////////////////////        
    }