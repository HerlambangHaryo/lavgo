<?php
    
 
    function UI_FORM_open_form($class){
        // ------------------------------------------------------------------------- INITIALIZE
            $isi = '<form class="'.$class.'"  method="POST">';
        // ------------------------------------------------------------------------- ACTION

        // ------------------------------------------------------------------------- SEND
        	$word = $isi;
        	return $word;            
        ////////////////////////////////////////////////////////////////////////////        
    }
 
    function UI_FORM_open_form_with_id($class,$id){
        // ------------------------------------------------------------------------- INITIALIZE
            $isi = '<form class="'.$class.'" method="POST" id="'.$id.'">';
        // ------------------------------------------------------------------------- ACTION

        // ------------------------------------------------------------------------- SEND
        	$word = $isi;
        	return $word;            
        ////////////////////////////////////////////////////////////////////////////        
    }

    function UI_FORM_open_form_enctype($class,$id){
        // ------------------------------------------------------------------------- INITIALIZE
            $isi = '<form class="'.$class.'" method="POST" id="'.$id.'" enctype="multipart/form-data">';
        // ------------------------------------------------------------------------- ACTION

        // ------------------------------------------------------------------------- SEND
            $word = $isi;
            return $word;            
        ////////////////////////////////////////////////////////////////////////////        
    }

    function UI_FORM_close_form(){
        // ------------------------------------------------------------------------- INITIALIZE
    		$isi = '</form>';
        // ------------------------------------------------------------------------- ACTION

        // ------------------------------------------------------------------------- SEND
        	$word = $isi;
        	return $word;            
        ////////////////////////////////////////////////////////////////////////////        
    }