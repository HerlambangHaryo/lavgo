<?php

    function button_submit_form($VALUE,$SUBMIT,$NAME,$LABEL,$CLASS)
    {
        // ------------------------------------------------------------------------- INITIALIZE
            $isi    = '';

        // ------------------------------------------------------------------------- ACTION
            $isi    .= ' 
            <button 
                type="submit" 
                class="'.$CLASS.'"
                value="'.$VALUE.'"
                id="'.$SUBMIT.'"
                name="'.$NAME.'">
                    '.$LABEL.'
            </button>
            ';
        // ------------------------------------------------------------------------- SEND
            $word = $isi;
            return $word;
		//////////////////////////////////////////////////////////////////////////// 		
    }

